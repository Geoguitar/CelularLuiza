    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

        boolean tV;
        boolean radio;        
        String marca;
        
        tV = this.CheckBoxTV.isSelected();
        radio = this.CheckBoxRadio.isSelected();
        marca = cmbMarca.getSelectedItem().toString();
        
        Cls_Calcular obj_Calcular = new Cls_Calcular();
        
        
       
        float valorCalculado = obj_Calcular.calculoValor(marca, tV, radio);

       txtValor.setText(String.valueOf(valorCalculado));

    }//GEN-LAST:event_btnCalcularActionPerformed

